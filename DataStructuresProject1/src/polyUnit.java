public class polyUnit {
      private double c;
      private int e;
      public polyUnit(){
         c=0;
         e=0;
      }
      public polyUnit(double c, int e){
         this.c = c;
         this.e = e;
      }
      public double get_c(){return c;}
      public int get_e(){return e;}
      public void set_c(double c){this.c = c;}
      public void set_e(int e){this.e = e;}
   }
 